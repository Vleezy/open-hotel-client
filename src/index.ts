function mapObject(obj: any, fn: Function, path: string[] = []) {
  for (let k in obj) {
    const currentPath = path.concat(k)
    if (obj[k].constructor === Object) {
      mapObject(obj[k], fn, currentPath)
    } else {
      fn(obj[k], currentPath)
    }
  }
}

